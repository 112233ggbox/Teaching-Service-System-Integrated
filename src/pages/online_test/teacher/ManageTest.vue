          <el-input placeholder="请输入考试名称" v-model="examInfo.testName"></el-input>
        </div>

        <!-- 课程选择 -->
        <div class="form-item">
          <label>选择课程</label>
          <el-select
              v-model="examInfo.courseId"
              placeholder="请选择课程"
              :loading="coursesLoading"
              @change="handleCourseChange"
          >
            <el-option
                v-for="course in courses"
                :key="course.id"
                :label="course.name"
                :value="course.id"
            />
          </el-select>
        </div>

        <!-- 章节选择 -->
        <div class="form-item">
          <label>选择章节</label>
          <el-select v-model="examInfo.chapterId" placeholder="请选择章节">
            <el-option
                v-for="chapter in chapters"
                :key="chapter.id"
                :label="`第 ${chapter.id} 章`"
                :value="chapter.id"
            />
          </el-select>
        </div>
      </div>

      <!-- 考试时长和时间 -->
      <div class="form-row">
        <div class="form-item">
          <label>考试时长</label>
          <el-input-number
              v-model="examInfo.duration"
              :min="10"
              :max="180"
              placeholder="请输入时长"
          >
            <template #append>分钟</template>
          </el-input-number>
        </div>

        <div class="form-item">
          <label>考试时间</label>
          <el-date-picker
              v-model="examInfo.startTime"
              type="datetime"
              placeholder="选择考试时间"
              format="YYYY-MM-DD HH:mm"
              value-format="YYYY-MM-DDTHH:mm:ss"
          />
        </div>
      </div>

      <!-- 是否开卷和操作按钮 -->
      <div class="form-row">
        <div class="form-item">
          <label>是否开卷</label>
          <el-select placeholder="请选择" v-model="examInfo.isOpenBook">
            <el-option label="开卷" :value="true"></el-option>
            <el-option label="闭卷" :value="false"></el-option>
          </el-select>
        </div>

        <div class="form-item">
          <label>试卷管理</label>
          <el-button type="primary" @click="showDialog = true">编辑试卷</el-button>
        </div>

        <div class="form-item">
          <label>自动生成</label>
          <el-button type="success" @click="showAutoGenerateDialog = true">自动生成试卷</el-button>
        </div>

<!--        <div class="form-item">-->
<!--          <label>查看试卷</label>-->
